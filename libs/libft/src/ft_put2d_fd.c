#include "libft.h"

void	ft_put2d_fd(char **s2d, int fd, size_t len)
{
	size_t  i;

	i = 0;
	while (i < len)
	{
		ft_putendl_fd(s2d[i], fd);
		i++;
	}
	return ;
}
